export function translate(language) {
	const innerHtmlElements = document.querySelectorAll("[data-translate_inner]");
	const inputFieldsElements = document.querySelectorAll("[data-translate_placeholder]");

	innerHtmlElements.forEach((element) => {
		let translated = translation[element.dataset.translate_inner];
		if (translated && translated[language]) {
			if (element.dataset.translate_inner === "friendRequest" || element.dataset.translate_inner === "gameRequest") {
				let words = element.innerHTML.split(" "); // Split the string into an array of words
				let username = words[words.length - 2];
				element.innerHTML = translated[language] + " " + username + " !";
			} else element.innerHTML = translated[language];
		} else element.innerHTML = "ERROR";
	});

	inputFieldsElements.forEach((element) => {
		let translated = translation[element.dataset.translate_placeholder];
		if (translated && translated[language]) {
			element.placeholder = translated[language];
		} else element.placeholder = "ERROR";
	});
}
